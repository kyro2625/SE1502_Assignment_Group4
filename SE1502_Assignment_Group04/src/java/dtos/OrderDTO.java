package dtos;

/**
 *
 * @author nguye
 */
public class OrderDTO {
    private int id;
    private String date, userID;

    public OrderDTO() {
    }

    public OrderDTO(int id, String date, String userID) {
        this.id = id;
        this.date = date;
        this.userID = userID;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }
    
    
}
